import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are an expert trading assistant for NSYNC JOURNAL, a trading journal application. Your role is to:

1. **Analyze Trading Performance**: When given trade data, provide insights on:
   - Win rate analysis and patterns
   - Risk management assessment
   - Best performing pairs, sessions, and strategies
   - Areas for improvement
   - Psychological patterns (overtrading, revenge trading, etc.)

2. **Provide Trading Insights**: Offer actionable advice based on the user's trading history:
   - Identify profitable patterns
   - Suggest strategy refinements
   - Highlight weaknesses in execution
   - Recommend focus areas

3. **Answer Trading Questions**: Help with general trading concepts:
   - Technical analysis
   - Risk management principles
   - Trading psychology
   - Position sizing

4. **Be Helpful & Concise**: Keep responses clear and actionable. Use bullet points for clarity. When analyzing data, provide specific numbers and percentages.

Always be encouraging but honest. If there are issues in trading performance, address them constructively.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, tradeContext } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    // Build context message if trade data is provided
    let contextMessage = "";
    if (tradeContext) {
      contextMessage = `\n\nCurrent Trading Data Context:\n${JSON.stringify(tradeContext, null, 2)}`;
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: SYSTEM_PROMPT + contextMessage },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Trading assistant error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
